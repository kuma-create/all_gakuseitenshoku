                      handleTemplateSelect("„ÅîË≥™Âïè„ÅÇ„Çä„Åå„Å®„ÅÜ„Åî„Åñ„ÅÑ„Åæ„Åô„ÄÇË©≥Á¥∞„Å´„Å§„ÅÑ„Å¶„ÅØ‰ª•‰∏ã„Çí„ÅîÁ¢∫Ë™ç„Åè„Å†„Åï„ÅÑ„ÄÇ")
